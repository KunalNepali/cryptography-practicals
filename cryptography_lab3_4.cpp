#include <iostream>
using namespace std;

int main() {
    int num;
    
    // Taking user input
    cout << "Enter a number: ";
    cin >> num;

    // Calculating additive inverse
    int additive_inverse = -num;

    // Displaying result
    cout << "The additive inverse of " << num << " is: " << additive_inverse << endl;

    return 0;
}

